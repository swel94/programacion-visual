

print("Digite un numero del 1 al 7 si desea saber el dia de la semana")
n = int(input())

if n == 1:
    print("Lunes")
elif n == 2:
    print("Martes")
elif n == 3:
    print("Miercoles")
elif n == 4:
    print("Jueves")
elif n == 5:
    print("Viernes")
elif n == 6:
    print("Sabado")
elif n == 7:
    print("Domingo")
else:
    print("Numero fuera de rango!")